package ErsteAufgaben;

public class Dreieck {

	public static void main(String[] args) {
		// Flächeninhalt des Dreiecks ausrechnen
		
		// Deklaration der benötigten Werte
		double g;
		double h;
		double a;
		
		// Wertzuweisung
		g = 5.6;
		h = 7.8;
		a = g * h / 2;
		
		// Ausgabe
		System.out.println(a = g * h / 2);		
		
		
	}

}

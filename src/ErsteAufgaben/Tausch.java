package ErsteAufgaben;

public class Tausch {

	public static void main(String[] args) {
		// Deklaration der Variablen
		int a;
		int b;
		int hilfsetui;
		
		// Wertzuweisung
		a = 5;
		b = 3;
		
		// Rechnung
		hilfsetui=a;
		a=b;
		b=hilfsetui;
		
		// Ausgabe
		System.out.println( "a=" + a );
		System.out.println("b=" + b);
		
		

	}

}
